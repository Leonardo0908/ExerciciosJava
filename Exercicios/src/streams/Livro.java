package streams;

public class Livro {
	
	final String nome;
	final double preco;
	final int codigo;
	final int quantidade;
	
	public Livro(String nome, double preco, int codigo, int quantidade) {
		super();
		this.nome = nome;
		this.preco = preco;
		this.codigo = codigo;
		this.quantidade = quantidade;
	}
}
